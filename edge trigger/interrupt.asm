ORG 0000H
		LJMP MAIN
ORG 0003H
	   	CPL P1.4
		RETI

ORG 30H
MAIN:	MOV IE,#81H        ; ENABLE EINT0
		SETB IT0           ; EDGE TRIGGERED INTERRUPT
AGAIN:	SETB P1.3
		ACALL Delay
		CLR P1.3
		ACALL Delay
		SJMP AGAIN


DELAY:      MOV R0,#07
	   HERE3:MOV R1,#255
	   HERE2:MOV R2,#255
	   HERE1:DJNZ R2,HERE1
	         DJNZ R1,HERE2
	         DJNZ R0,HERE3
	   RET

END
